package com.example.forca.Model;

public class Identificador {
	int idIdentificador;
    int NivelJogo;
    String urlIdentificador;

    public int getIdIdentificador() {
        return idIdentificador;
    }

    public void setIdIdentificador(int idIdentificador) {
        this.idIdentificador = idIdentificador;
    }

    public int getNivelJogo() {
        return NivelJogo;
    }

    public void setNivelJogo(int NivelJogo) {
        this.NivelJogo = NivelJogo;
    }

    public String getUrlIdentificador() {
        return urlIdentificador;
    }

    public void setUrlIdentificador(String urlIdentificador) {
        this.urlIdentificador = urlIdentificador;
    }
}
